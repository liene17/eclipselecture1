
public class SwitchExample {

	
	public static void main(String[] args) {
		int expression = 2;
		int result = 0;
		switch (expression) {
		case 1:
			result++;
			break;
		case 2:
			result++;
			break;
		case 3:
			result++;
			break;
		default:
			result++;
		}
		
	
		System.out.println(result);
		
	}

}
